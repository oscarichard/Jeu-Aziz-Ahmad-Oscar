### Comment jouer :
# le fleche du haut permait de tirer, les fleche latérale permette de se déplacer
# il faut eviter les kryptonites (elles bloque les tires) et tirer sur les vaisseaux
# si vous atteigner un score de 10, 20, 30... un boss arrive et les chance que des vaissaux spawn augmente
# le boss vaut +5 point quand détruit et -1 quand il ne l'ait pas
# les vaisseaux valent +1 point quand ils sont détruit et -1 quand ils ne le sont pas
